pub fn add(double a, double b) f64 {
    return a + b;
}

pub fn sub(double a, double b) f64 {
    return a - b;
}

pub fn mul(double a, double b) f64 {
    return a * b;
}

pub fn div(double a, double b) f64 {
    return a / b;
}

pub fn neg(double a) f64 {
    return -a;
}

pub fn comma(double a, double b) f64 {
    return b;
}